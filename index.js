var data = [],
  player = {},
  playerSummary = {}
playerDetails = {}
playerInfo = {}

urlList.forEach((url) => {
  axios.get("https://www.futhead.com/22/players/" + url).then((res) => {
    const $ = cheerio.load(res.data)

    //#region Summary
    for (let i = 1; i < 7; i++) {
      playerSummary[
        $(`.igs-group:nth-child(${i}) h5`).text().trim().toLowerCase()
      ] =
        $(`.playercard-attr.playercard-attr${i} .chembot-value`).text().trim() *
        1
    }
    //#endregion

    //#region Details
    $(".player-stat-row").each((i, e) => {
      const temp = $(e)
        .text()
        .trim()
        .replace("\n", "")
        .split(" ")
        .filter((e) => {
          return e !== ""
        })

      if (temp.length === 3) {
        temp[0] = temp[0].replace(".", "").toLowerCase() + temp[1]
        temp[1] = temp[2]
        temp.pop()
      } else {
        temp[0] = temp[0].toLowerCase()
      }

      playerDetails[temp[0]] = temp[1] * 1
    })
    //#endregion

    //#region Info
    for (let i = 0; i < $(`#info-tab .player-sidebar-item`).length; i++) {
      const s = $(`#info-tab .player-sidebar-item:nth-child(${i}) .col-xs-7`)
        .text()
        .trim()
        .toLowerCase()

      switch (s) {
        case "club":
          playerInfo[s] = $(
            `#info-tab .player-sidebar-item:nth-child(${i}) .futhead-link`
          )
            .text()
            .trim()
            .replace("Spurs", "Tottenham")
          break

        case "league":
          playerInfo[s] = $(
            `#info-tab .player-sidebar-item:nth-child(${i}) .futhead-link`
          )
            .text()
            .trim()
          break

        case "nation":
          playerInfo[s] = $(
            `#info-tab .player-sidebar-item:nth-child(${i}) .futhead-link`
          )
            .text()
            .trim()
          break

        case "strong foot":
          playerInfo[s.replace("strong ", "")] = $(
            `#info-tab .player-sidebar-item:nth-child(${i}) .player-sidebar-value`
          )
            .text()
            .trim()
            .toLowerCase()
          break

        case "age":
          playerInfo["birthday"] =
            $(
              `#info-tab .player-sidebar-item:nth-child(${i}) .player-sidebar-value`
            )
              .text()
              .trim()
              .toLowerCase()
              .slice(-4) * 1
          break

        case "height":
          playerInfo[s] =
            $(
              `#info-tab .player-sidebar-item:nth-child(${i}) .player-sidebar-value`
            )
              .text()
              .split(" | ")[0]
              .replace("cm", "") * 1
          break

        case "skill moves":
          playerInfo["skillsMoves"] = $(
            `#info-tab .player-sidebar-item:nth-child(${i}) .player-sidebar-value .icon.icon-star.gold`
          ).length
          break

        case "weak foot":
          playerInfo["weakFoot"] = $(
            `#info-tab .player-sidebar-item:nth-child(${i}) .player-sidebar-value .icon.icon-star.gold`
          ).length
          break

        default:
          break
      }
    }
    //#endregion

    var name = $(`.playercard-name`).first().text().trim().toLowerCase()
    var fullname = $(`.font-16.fh-red`).text().trim()
    var position = $(`.playercard-position`).first().text().trim()

    player.fullname = fullname
    player.name = name.charAt(0).toUpperCase() + name.slice(1)
    player.position = position
    player.info = playerInfo
    player.summary = playerSummary
    player.details = playerDetails

    data.push(player)

    player = {}
    playerSummary = {}
    playerDetails = {}
    playerInfo = {}

    var fs = require("fs")
    var stream = fs.createWriteStream("players.json")
    stream.once("open", function (fd) {
      stream.write(JSON.stringify(data))
      stream.end()
    })

    console.log("Toplam " + data.length + " oyuncu verisi hazır.")
  })
})
